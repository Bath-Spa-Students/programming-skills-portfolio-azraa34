# Write a python program that stores fruit names in a list named fruits. Unpack the list into three
# variables x, y and z and then print the values of each variable.

# Store fruit names in a list named fruits
fruits = ['Mango', 'Melon', 'Peach']

# Unpack the list into three variables x, y, and z
x, y, z = fruits

# Print the values of each variable
print("Value of x:", x)
print("Value of y:", y)
print("Value of z:", z)