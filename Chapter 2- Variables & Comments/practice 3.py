# Write a python program that takes an input 5 from user and then type cast those values to string,
# int and float in the separate variables. Print all the variables.

# Take input from the user
value_1 = input("Enter the number 5 : ")

# Typecast to string, int, and float
string_var = str(value_1)
int_var = int(value_1)
float_var = float(value_1)

# Print all variables
print("\nString variable:", string_var)
print("Integer variable:", int_var)
print("Float variable:", float_var)